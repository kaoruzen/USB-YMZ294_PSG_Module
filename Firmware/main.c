/********************************************************************
 * Function:        static void InitializeSystem(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        InitializeSystem is a centralize initialization
 *                  routine. All required USB initialization routines
 *                  are called from here.
 *
 *                  User application initialization routine should
 *                  also be called from here.                  
 *
 * Note:            None
 *******************************************************************/
static void InitializeSystem(void)
{
    #if (defined(__18CXX) & !defined(PIC18F87J50_PIM))
        ADCON1 |= 0x0F;                 // Default all pins to digital
    #elif defined(__C30__)
        AD1PCFGL = 0xFFFF;
    #elif defined(__C32__)
        AD1PCFG = 0xFFFF;
    #endif


//	The USB specifications require that USB peripheral devices must never source
//	current onto the Vbus pin.  Additionally, USB peripherals should not source
//	current on D+ or D- when the host/hub is not actively powering the Vbus line.
//	When designing a self powered (as opposed to bus powered) USB peripheral
//	device, the firmware should make sure not to turn on the USB module and D+
//	or D- pull up resistor unless Vbus is actively powered.  Therefore, the
//	firmware needs some means to detect when Vbus is being powered by the host.
//	A 5V tolerant I/O pin can be connected to Vbus (through a resistor), and
// 	can be used to detect when Vbus is high (host actively powering), or low
//	(host is shut down or otherwise not supplying power).  The USB firmware
// 	can then periodically poll this I/O pin to know when it is okay to turn on
//	the USB module/D+/D- pull up resistor.  When designing a purely bus powered
//	peripheral device, it is not possible to source current on D+ or D- when the
//	host is not actively providing power on Vbus. Therefore, implementing this
//	bus sense feature is optional.  This firmware can be made to use this bus
//	sense feature by making sure "USE_USB_BUS_SENSE_IO" has been defined in the
//	HardwareProfile.h file.    
    #if defined(USE_USB_BUS_SENSE_IO)
        // Following configure item is already defined "HardwareProfile - PIC18F2550.h".
        //tris_usb_bus_sense = INPUT_PIN; // See HardwareProfile.h
    #endif
    
//	If the host PC sends a GetStatus (device) request, the firmware must respond
//	and let the host know if the USB peripheral device is currently bus powered
//	or self powered.  See chapter 9 in the official USB specifications for details
//	regarding this request.  If the peripheral device is capable of being both
//	self and bus powered, it should not return a hard coded value for this request.
//	Instead, firmware should check if it is currently self or bus powered, and
//	respond accordingly.  If the hardware has been configured like demonstrated
//	on the PICDEM FS USB Demo Board, an I/O pin can be polled to determine the
//	currently selected power source.  On the PICDEM FS USB Demo Board, "RA2" 
//	is used for	this purpose.  If using this feature, make sure "USE_SELF_POWER_SENSE_IO"
//	has been defined in HardwareProfile.h, and that an appropriate I/O pin has been mapped
//	to it in HardwareProfile.h.
    #if defined(USE_SELF_POWER_SENSE_IO)
    tris_self_power = INPUT_PIN;	// See HardwareProfile.h
    #endif
    
    UserInit();

    USBDeviceInit();	//usb_device.c.  Initializes USB module SFRs and firmware
    					//variables to known states.
}//end InitializeSystem



/******************************************************************************
 * Function:        void UserInit(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        This routine should take care of all of the demo code
 *                  initialization that is required.
 *
 * Note:            
 *
 *****************************************************************************/
void UserInit(void)
{
	TRISA=0b00000000;       //PORTAは全出力
	TRISB=0b00000000;	//PORTBは全出力
	TRISC=0b00000000;	//PORTCはRX以外全出力

#if 1
	/* 割り込みに関する設定 */
	T0CON = 0b10000110;		// Timer0 16bit mode, prescaler 1/128.
		/*
		 * *** T0CONについて ***
		 *
		 * bit7: TMR0ON	1:TMR0有効	0:TMR0無効
		 * bit6: T08BIT	1:8bitMode	0:16bitMode
		 * bit5: T0CS		1:TMR0クロックソースがT0CKIピン	0:クロックソースが命令サイクル
		 * bit4: T0SE		1:T0CKIのときパルスの立ち上がりでカウント		0:立ち下がりでカウント
		 * bit3: PSA		1:プリスケーラを使用しない	0:プリスケーラ使用
		 * bit2-0:T0PS	000	x2
		 *				001	x4
		 *				010	x8
		 *				011	x16
		 *				100	x32
		 *				101	x64
		 *				110	x128
		 *				111	x256
		 * 参考：http://homepage3.nifty.com/mitt/pic/c18/c18_1320_02.html
		 */
	INTCONbits.TMR0IE = 1;	// Timer0 Interrupt Enable
		/*
		 * *** INTCONについて ***
		 *
		 * bit7: GIE		全割り込み禁止ビット	1:割り込み許可	0:全ての割り込み禁止
		 * bit6: PEIE	
		 * bit5: TMR0IE	1:TMR0割り込み有効	0:TMR0割り込み無効
		 * bit4: INT0IE
		 * bit3: RBIE
		 * bit2: TMR0IF	TMR0OverFlowフラグ	1:OverFlowが発生した	0:発生してない
		 * bit1: INT0IF
		 * bit0: RBIF
		 */
		 
		 /*
		  * *** 設定例 ***
		  *
		  * 8bitMode、プリスケーラ無しだと、
		  * クロックはPLLとDIVで96MHz/2=48MHz
		  * 1命令サイクル4クロックなので、命令サイクルは48/4=12MHz
		  * 周波数12MHzの周期は1/12M=0.08333uSec
		  * TMR0Lレジスタがオーバーフローしたときに割り込みが発生するので、
		  * 0.08333uSec x 2^8 = 0.08333uSec x 256 = 21.333uSec
		  * よって、上の設定例の場合は、21.33uSec(47.6kHz)ごとに割り込みが発生する。
		  */
        TMR0L = 0;

	RCONbits.IPEN = 1;		// 優先度割り込みを有効化
	INTCON2bits.TMR0IP = 0;	// 低優先度割り込みをTimer0に設定
	
	INTCONbits.GIEH = 1;    //高優先度割り込みEnable
	INTCONbits.GIEL = 1;    //低優先度割り込みEnable
#endif

    TRISB=0x00;
    INTCONbits.RBIF = 0;
    INTCON2bits.RBPU = 0;
    
    LED = 0;
    
    IC = 1;
    A0 = 1;
    CS = 1;
    WR = 1;
    BUS = 0x00;

}//end UserInit


/********************************************************************
 * Function:        void ProcessIO(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        This function is a place holder for other user
 *                  routines. It is a mixture of both USB and
 *                  non-USB tasks.
 *
 * Note:            None
 *******************************************************************/
void ProcessIO(void)
{   
    BYTE numBytesRead;					//読み出したバイト数
	char data = 0x00;
    char addr = 0x00;
    
    // User Application USB tasks
    if((USBDeviceState < CONFIGURED_STATE)||(USBSuspendControl==1)) return;

    if(mUSBUSARTIsTxTrfReady())
    {
		numBytesRead = getsUSBUSART(USB_In_Buffer,64);
		if(numBytesRead != 0)
		{
                    switch(USB_In_Buffer[0])
                    {
                        case 'R':       //RESET
                            LED = 1;
                            IC = 0;
                            Delay100TCYx(100);
                            IC = 1;
                            LED = 0;
                            break;
                        case 'D':       //データ書き込み
                            addr = USB_In_Buffer[1];
                            data = USB_In_Buffer[2];
                            
                            LED = ~LED;
                           
                            
                            CS = 0;
                            WR = 0;
                            A0 = 0;
                            BUS = addr;
                            
                            Delay10TCYx(10);
                            
                            CS = 1;
                            WR = 1;
                            
                            Delay10TCYx(10);
                            BUS = data;
                            Delay10TCYx(10);
                            
                            CS = 0;
                            WR = 0;
                            A0 = 1;
                            BUS = data;
                            
                            Delay10TCYx(10);
                            
                            CS = 1;
                            WR = 1;
                            
                            
                            Delay10TCYx(10);
                            A0 = 0;
                            CS = 1;
                            WR = 1;
                          
                            
                            //LED = 0;
                            
                            break;
                        case 'G':   //Debug Information Request (Not specified!)
                            //sprintf(USB_Out_Buffer, "B,%04d,%04d,%04u,%d,%04d,%d,%04d,%04u,%04d,%04u,%04u\r\n",
                            //        Futaba_Angle, Futaba_NextMovePosition, Futaba_NextMoveSpeed, Futaba_Flags, Futaba_Time, Futaba_Moving,
                            //        Futaba_SentPosition, Futaba_SentSpeed, Futaba_SentPacketSerial, EEPROM_UnloadSpeed, EEPROM_LoadSpeed);
                            //putUSBUSART(USB_Out_Buffer, strlen (USB_Out_Buffer));
                            break;
                        break;
                        default:
                            break;
                    }
		}
		
	}

    CDCTxService();
}		//end ProcessIO


